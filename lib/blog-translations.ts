const translations: Record<string, Partial<Record<Lang, BlogTranslation>>> = {};

for (const slug of Object.keys(ruTranslations)) {
  translations[slug] = {
    RU: ruTranslations[slug],
    UA: uaTranslations[slug],
  };
}

export function getBlogTranslation(slug: string, lang: Lang): BlogTranslation | null {
  if (lang === "EN") return null;
  return translations[slug]?.[lang] ?? null;
}
